
"use client";

import type { GenerateWorkoutScheduleOutput } from '@/ai/flows/generate-workout-schedule-types';
import { Card, CardContent, CardDescription, CardHeader, CardTitle, CardFooter } from '@/components/ui/card';
import { ScrollArea } from '@/components/ui/scroll-area';
import { Accordion, AccordionContent, AccordionItem, AccordionTrigger } from '@/components/ui/accordion';
import { Alert, AlertDescription, AlertTitle } from "@/components/ui/alert";
import { Button } from '@/components/ui/button';
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from "@/components/ui/table";
import { ClipboardList, CalendarDays, Dumbbell, Info, Copy, Check } from 'lucide-react';
import { useState, useEffect } from 'react';
import { useToast } from '@/hooks/use-toast';

interface WorkoutScheduleDisplayProps {
  scheduleOutput: GenerateWorkoutScheduleOutput;
}

export default function WorkoutScheduleDisplay({ scheduleOutput }: WorkoutScheduleDisplayProps) {
  const { scheduleTitle, weeklySchedule, notes, disclaimer } = scheduleOutput;
  const { toast } = useToast();
  const [isCopied, setIsCopied] = useState(false);

  // Find the first workout day to open by default in the accordion
  const firstWorkoutDay = weeklySchedule.find(day => day.exercises.length > 0)?.day;

  const formatScheduleForCopy = (): string => {
    let text = `${scheduleTitle || "Your Weekly Workout Schedule"}\n\n`;

    weeklySchedule.forEach(day => {
      text += `--- ${day.day}: ${day.focus} ---\n`;
      if (day.exercises.length > 0) {
        day.exercises.forEach(exercise => {
          text += `  - ${exercise.name}: ${exercise.sets} sets of ${exercise.reps} reps\n`;
        });
      } else {
        text += "  Rest Day\n";
      }
      text += "\n";
    });

    if (notes) {
      text += "Important Notes:\n";
      text += `${notes}\n\n`;
    }

    if (disclaimer) {
      text += "Disclaimer:\n";
      text += `${disclaimer}\n`;
    }
    return text;
  };

  const handleCopyToClipboard = async () => {
    const planText = formatScheduleForCopy();
    try {
      await navigator.clipboard.writeText(planText);
      toast({
        title: "Copied to Clipboard!",
        description: "The workout schedule has been copied.",
        variant: "default",
      });
      setIsCopied(true);
    } catch (err) {
      console.error('Failed to copy text: ', err);
      toast({
        title: "Copy Failed",
        description: "Could not copy the schedule to your clipboard.",
        variant: "destructive",
      });
      setIsCopied(false);
    }
  };

   useEffect(() => {
    if (isCopied) {
      const timer = setTimeout(() => {
        setIsCopied(false);
      }, 2500);
      return () => clearTimeout(timer);
    }
  }, [isCopied]);


  return (
    <Card id="workoutScheduleCardPrintable" className="mt-8 w-full max-w-3xl mx-auto shadow-xl">
      <CardHeader>
        <CardTitle className="flex items-center gap-2 text-2xl">
          <ClipboardList className="text-primary" />
          {scheduleTitle || "Your Weekly Workout Schedule"}
        </CardTitle>
        <CardDescription>
          Here is the weekly schedule generated by our AI. Remember to warm up before each session.
        </CardDescription>
      </CardHeader>
      <CardContent>
        <ScrollArea className="h-[500px] w-full p-1 border rounded-md bg-card print-expandable-scroll-area">
          <Accordion type="single" collapsible defaultValue={firstWorkoutDay} className="w-full p-3">
            {weeklySchedule.map((day) => (
              <AccordionItem value={day.day} key={day.day} className="AccordionItem">
                <AccordionTrigger className="AccordionTrigger">
                  <div className="flex items-center gap-3">
                    <CalendarDays className="w-5 h-5 text-primary" />
                    <div>
                      <h4 className="font-semibold text-md">{day.day}</h4>
                      <p className="text-sm text-muted-foreground font-normal">{day.focus}</p>
                    </div>
                  </div>
                </AccordionTrigger>
                <AccordionContent className="AccordionContent">
                  {day.exercises.length > 0 ? (
                    <Table>
                      <TableHeader>
                        <TableRow>
                          <TableHead>Exercise</TableHead>
                          <TableHead className="text-center">Sets</TableHead>
                          <TableHead className="text-center">Reps</TableHead>
                        </TableRow>
                      </TableHeader>
                      <TableBody>
                        {day.exercises.map((exercise, index) => (
                          <TableRow key={index}>
                            <TableCell className="font-medium">{exercise.name}</TableCell>
                            <TableCell className="text-center">{exercise.sets}</TableCell>
                            <TableCell className="text-center">{exercise.reps}</TableCell>
                          </TableRow>
                        ))}
                      </TableBody>
                    </Table>
                  ) : (
                    <div className="p-4 text-center text-muted-foreground">
                      <p>Rest Day - a great time to recover and grow stronger!</p>
                    </div>
                  )}
                </AccordionContent>
              </AccordionItem>
            ))}
          </Accordion>

          {notes && (
            <div className="mt-6 p-4 border-t">
              <h3 className="flex items-center gap-2 text-lg font-semibold text-primary mb-2">
                <Dumbbell className="w-5 h-5" />
                Important Notes
              </h3>
              <p className="text-sm whitespace-pre-line leading-relaxed text-muted-foreground">{notes}</p>
            </div>
          )}
          
        </ScrollArea>
        
        {disclaimer && (
          <Alert variant="default" className="mt-6">
            <Info className="h-4 w-4" />
            <AlertTitle>Disclaimer</AlertTitle>
            <AlertDescription>
              {disclaimer}
            </AlertDescription>
          </Alert>
        )}
         
      </CardContent>
      <CardFooter className="justify-end pt-4">
        <Button onClick={handleCopyToClipboard} variant="outline" className="print-hide-button">
          {isCopied ? <Check className="mr-2 h-4 w-4 text-green-500" /> : <Copy className="mr-2 h-4 w-4" />}
          {isCopied ? 'Copied!' : 'Copy Schedule'}
        </Button>
      </CardFooter>
    </Card>
  );
}
