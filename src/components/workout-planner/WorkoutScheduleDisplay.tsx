
"use client";

import type { GenerateWorkoutScheduleOutput } from '@/ai/flows/generate-workout-schedule-types';
import { Card, CardContent, CardDescription, CardHeader, CardTitle, CardFooter } from '@/components/ui/card';
import { ScrollArea } from '@/components/ui/scroll-area';
import { Alert, AlertDescription, AlertTitle } from "@/components/ui/alert";
import { Button } from '@/components/ui/button';
import { ClipboardList, Zap, Info, Copy, Check } from 'lucide-react';
import { useState, useEffect } from 'react';
import { useToast } from '@/hooks/use-toast';

interface WorkoutScheduleDisplayProps {
  scheduleOutput: GenerateWorkoutScheduleOutput;
}

export default function WorkoutScheduleDisplay({ scheduleOutput }: WorkoutScheduleDisplayProps) {
  const { workoutTitle, exercises, notes, disclaimer } = scheduleOutput;
  const { toast } = useToast();
  const [isCopied, setIsCopied] = useState(false);

  const formatScheduleForCopy = (): string => {
    let text = `${workoutTitle || "Workout Session"}\n\n`;

    exercises.forEach(exercise => {
      text += `${exercise.name}\n`;
      text += `  - Sets: ${exercise.sets}\n`;
      text += `  - Reps: ${exercise.reps}\n`;
      text += `  - Rest: ${exercise.rest}\n\n`;
    });

    if (notes) {
      text += "Important Notes:\n";
      text += `${notes}\n\n`;
    }

    if (disclaimer) {
      text += "Disclaimer:\n";
      text += `${disclaimer}\n`;
    }
    return text;
  };

  const handleCopyToClipboard = async () => {
    const planText = formatScheduleForCopy();
    try {
      await navigator.clipboard.writeText(planText);
      toast({
        title: "Copied to Clipboard!",
        description: "The workout session has been copied.",
        variant: "default",
      });
      setIsCopied(true);
    } catch (err) {
      console.error('Failed to copy text: ', err);
      toast({
        title: "Copy Failed",
        description: "Could not copy the workout session to your clipboard.",
        variant: "destructive",
      });
      setIsCopied(false);
    }
  };

  useEffect(() => {
    if (isCopied) {
      const timer = setTimeout(() => {
        setIsCopied(false);
      }, 2500); // Reset after 2.5 seconds
      return () => clearTimeout(timer);
    }
  }, [isCopied]);


  return (
    <Card id="workoutScheduleCardPrintable" className="mt-8 w-full max-w-3xl mx-auto shadow-xl">
      <CardHeader>
        <CardTitle className="flex items-center gap-2 text-2xl">
          <ClipboardList className="text-primary" />
          {workoutTitle || "Your Personalized Workout Session"}
        </CardTitle>
        <CardDescription>
          Here is the workout generated by our AI. Remember to warm up before starting and cool down afterwards.
        </CardDescription>
      </CardHeader>
      <CardContent>
        <ScrollArea className="h-[500px] w-full p-1 border rounded-md bg-card print-expandable-scroll-area">
          <div className="p-4 space-y-4">
              {exercises.map((exercise, index) => (
                  <div key={index} className="p-3 bg-muted/50 rounded-lg shadow-sm border">
                      <h4 className="text-lg font-semibold text-primary">{exercise.name}</h4>
                      <div className="mt-2 grid grid-cols-3 gap-2 text-sm text-center">
                          <div className="bg-background/50 p-2 rounded">
                              <p className="font-bold text-foreground">{exercise.sets}</p>
                              <p className="text-xs text-muted-foreground">Sets</p>
                          </div>
                          <div className="bg-background/50 p-2 rounded">
                              <p className="font-bold text-foreground">{exercise.reps}</p>
                              <p className="text-xs text-muted-foreground">Reps</p>
                          </div>
                          <div className="bg-background/50 p-2 rounded">
                              <p className="font-bold text-foreground">{exercise.rest}</p>
                              <p className="text-xs text-muted-foreground">Rest</p>
                          </div>
                      </div>
                  </div>
              ))}
          </div>

          {notes && (
            <div className="mt-6 p-4 border-t">
              <h3 className="flex items-center gap-2 text-lg font-semibold text-primary mb-2">
                <Zap className="w-5 h-5" />
                Important Notes
              </h3>
              <p className="text-sm whitespace-pre-line leading-relaxed text-muted-foreground">{notes}</p>
            </div>
          )}
          
        </ScrollArea>
        
        {disclaimer && (
          <Alert variant="default" className="mt-6">
            <Info className="h-4 w-4" />
            <AlertTitle>Disclaimer</AlertTitle>
            <AlertDescription>
              {disclaimer}
            </AlertDescription>
          </Alert>
        )}
         
      </CardContent>
      <CardFooter className="justify-end pt-4">
        <Button onClick={handleCopyToClipboard} variant="outline" className="print-hide-button">
          {isCopied ? <Check className="mr-2 h-4 w-4 text-green-500" /> : <Copy className="mr-2 h-4 w-4" />}
          {isCopied ? 'Copied!' : 'Copy Workout'}
        </Button>
      </CardFooter>
    </Card>
  );
}
